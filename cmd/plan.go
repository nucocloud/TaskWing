// ── plan delete ──────────────────────────────────────────────────────────────

var planDeleteCmd = &cobra.Command{
	Use:   "delete [plan-id]",
	Short: "Delete a plan and its tasks",
	Args:  cobra.ExactArgs(1),
	RunE:  runPlanDelete,
}

func runPlanDelete(cmd *cobra.Command, args []string) error {
	repo, err := openRepoOrHandleMissingMemory()
	if err != nil || repo == nil {
		return err
	}
	defer func() { _ = repo.Close() }()

	planID, err := resolvePlanID(repo, args[0])
	if err != nil {
		return err
	}

	force, _ := cmd.Flags().GetBool("force")
	if !force && !isJSON() {
		p, err := repo.GetPlan(planID)
		if err != nil {
			return err
		}
		fmt.Printf("\n  plan   %s\n", p.ID)
		fmt.Printf("  goal   %s\n", p.Goal)
		fmt.Printf("  tasks  %d\n\n", p.GetTaskCount())
		if !confirmOrAbort("delete this plan and all its tasks? [y/N]: ") {
			return nil
		}
	}

	if err := repo.DeletePlan(planID); err != nil {
		return fmt.Errorf("delete plan: %w", err)
	}

	if isJSON() {
		return printJSON(map[string]any{"id": planID, "deleted": true})
	}
	fmt.Printf("status   deleted\n")
	fmt.Printf("id       %s\n", planID)
	return nil
}

// ── plan tool (JSON-blob entry, used by the /taskwing:plan slash command) ────

var planParamsRaw string

var planToolCmd = &cobra.Command{
	Use:   "tool",
	Short: "Run a plan action from a JSON params blob (advanced, used by slash commands)",
	Long: `Run a plan action by passing the full params object as JSON.

This is the entry point the /taskwing:plan slash command uses. Humans
typically want one of the higher-level subcommands (list, show, current,
set-active, delete) and the /taskwing:plan flow itself for clarify and
generate. Reach for this only when you need the raw tool contract.

Actions: clarify, decompose, expand, generate, finalize, audit.`,
	Example: `  taskwing plan tool --params '{"action":"clarify","goal":"add stripe billing"}'
  echo '{"action":"decompose","clarify_session_id":"...","enriched_goal":"..."}' \
    | taskwing plan tool --params -`,
	RunE: runPlanTool,
}

func runPlanTool(cmd *cobra.Command, args []string) error {
	raw, err := readParamsBlob(planParamsRaw)
	if err != nil {
		return err
	}

	var params handlers.PlanToolParams
	if err := json.Unmarshal(raw, &params); err != nil {
		return fmt.Errorf("parse --params JSON: %w", err)
	}

	repo, err := openRepoOrHandleMissingMemory()
	if err != nil || repo == nil {
		return err
	}
	defer func() { _ = repo.Close() }()

	ctx, cancel := context.WithTimeout(cmd.Context(), 5*time.Minute)
	defer cancel()

	result, err := handlers.HandlePlanTool(ctx, repo, params)
	if err != nil {
		return err
	}

	return printJSON(result)
}

