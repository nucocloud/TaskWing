// renderPlanRecord prints a Plan as a compact key:value record, mirroring
// the shape of renderTaskRecord. Phases and tasks render as nested lists.
func renderPlanRecord(p *task.Plan) {
	r := render.NewRecord(os.Stdout)
	r.Inline("id", p.ID)
	r.Inline("status", string(p.Status))
	r.Inlinef("tasks", "%d", p.GetTaskCount())
	if !p.CreatedAt.IsZero() {
		r.Inline("created", p.CreatedAt.Format("2006-01-02 15:04"))
	}
	if !p.UpdatedAt.IsZero() && !p.UpdatedAt.Equal(p.CreatedAt) {
		r.Inline("updated", p.UpdatedAt.Format("2006-01-02 15:04"))
	}
	if p.GenerationMode != "" {
		r.Inline("mode", string(p.GenerationMode))
	}
	r.End()

	if p.Goal != "" {
		fmt.Println()
		fmt.Println("goal")
		fmt.Printf("  %s\n", p.Goal)
	}
	if p.EnrichedGoal != "" && p.EnrichedGoal != p.Goal {
		fmt.Println()
		fmt.Println("enriched-goal")
		for _, line := range strings.Split(strings.TrimSpace(p.EnrichedGoal), "\n") {
			fmt.Printf("  %s\n", line)
		}
	}

	if len(p.Phases) > 0 {
		fmt.Println()
		fmt.Printf("phases (%d)\n", len(p.Phases))
		for i, ph := range p.Phases {
			fmt.Printf("  %d. %s\n", i+1, ph.Title)
			if ph.Description != "" {
				fmt.Printf("     %s\n", ph.Description)
			}
		}
	}

	if len(p.Tasks) > 0 {
		fmt.Println()
		fmt.Printf("tasks (%d)\n", len(p.Tasks))
		tbl := render.NewTable(os.Stdout, []string{"  ID", "STATUS", "PRI", "TITLE"}).
			Width(100).
			RightAlign(2)
		for _, t := range p.Tasks {
			tbl.Row(
				"  "+utils.ShortID(t.ID, utils.TaskIDLength),
				string(t.Status),
				fmt.Sprintf("%d", t.Priority),
				t.Title,
			)
		}
		tbl.Render()
	}
}

// resolvePlanID accepts a full plan ID or a unique prefix and returns the
// matching ID. Errors when zero or multiple plans match.
func resolvePlanID(repo planLookup, idOrPrefix string) (string, error) {
	plans, err := repo.ListPlans()
	if err != nil {
		return "", err
	}
	var matches []string
	for _, p := range plans {
		if p.ID == idOrPrefix {
			return p.ID, nil
		}
		if strings.HasPrefix(p.ID, idOrPrefix) {
			matches = append(matches, p.ID)
		}
	}
	switch len(matches) {
	case 0:
		return "", fmt.Errorf("no plan matches %q", idOrPrefix)
	case 1:
		return matches[0], nil
	default:
		return "", fmt.Errorf("plan id %q is ambiguous: %s", idOrPrefix, strings.Join(matches, ", "))
	}
}

// planLookup is the minimal interface we need from the memory repo for plan
// commands. Lets resolvePlanID stay testable without dragging in the full repo.
type planLookup interface {
	ListPlans() ([]task.Plan, error)
}

// readParamsBlob reads a JSON params blob from a flag value. If the value is
// "-" it reads from stdin instead. Returns the raw bytes for json.Unmarshal.
func readParamsBlob(flagValue string) ([]byte, error) {
	if flagValue == "-" {
		data, err := io.ReadAll(os.Stdin)
		if err != nil {
			return nil, fmt.Errorf("read stdin: %w", err)
		}
		return data, nil
	}
	if strings.TrimSpace(flagValue) == "" {
		return nil, fmt.Errorf("--params is required (or pass '-' to read from stdin)")
	}
	return []byte(flagValue), nil
}
