// planCmd is the parent for plan management subcommands.
//
// Human-facing subcommands mirror `taskwing task`:
//
//	plan list           list all plans
//	plan show <id>      show one plan in detail
//	plan current        show the active plan
//	plan set-active <id> switch the active plan
//	plan delete <id>    delete a plan and its tasks
//
// The JSON-blob entry point used by the /taskwing:plan slash command lives at
// `plan tool --params <json>` so it's still callable from skills.
var planCmd = &cobra.Command{
	Use:   "plan",
	Short: "Manage plans (list, show, current, set-active, delete, tool)",
	Long: `Manage TaskWing plans.

Plans group related tasks generated by the clarify-decompose-expand flow.
The list/show/current subcommands give a human-friendly view; the tool
subcommand exposes the JSON-blob entry point used by the /taskwing:plan
slash command in your AI tool.`,
}

func init() {
	rootCmd.AddCommand(planCmd)
	planCmd.AddCommand(planListCmd)
	planCmd.AddCommand(planShowCmd)
	planCmd.AddCommand(planCurrentCmd)
	planCmd.AddCommand(planSetActiveCmd)
	planCmd.AddCommand(planDeleteCmd)
	planCmd.AddCommand(planToolCmd)

	planToolCmd.Flags().StringVar(&planParamsRaw, "params", "", "JSON params blob; use '-' to read from stdin")
	_ = planToolCmd.MarkFlagRequired("params")

	planDeleteCmd.Flags().Bool("force", false, "Skip confirmation prompt")
}

// ── plan list ────────────────────────────────────────────────────────────────

var planListCmd = &cobra.Command{
	Use:   "list",
	Short: "List all plans",
	Long: `List every plan with id, status, task count, and goal.

Examples:
  taskwing plan list`,
	RunE: runPlanList,
}

func runPlanList(cmd *cobra.Command, args []string) error {
	repo, err := openRepoOrHandleMissingMemory()
	if err != nil || repo == nil {
		return err
	}
	defer func() { _ = repo.Close() }()

	plans, err := repo.ListPlans()
	if err != nil {
		return fmt.Errorf("list plans: %w", err)
	}

	if isJSON() {
		return printJSON(plans)
	}

	if len(plans) == 0 {
		fmt.Println("no plans found. use /taskwing:plan in your AI tool to create one.")
		return nil
	}

	active, _ := repo.GetActivePlan()
	activeID := ""
	if active != nil {
		activeID = active.ID
	}

	if ui.IsInteractive() {
		ui.SectionHeader("Plans")
	}

	tbl := render.NewTable(os.Stdout, []string{"ID", "STATUS", "TASKS", "CREATED", "GOAL"}).
		Width(100).
		RightAlign(2)
	for _, p := range plans {
		idCell := utils.ShortID(p.ID, utils.TaskIDLength)
		if p.ID == activeID {
			idCell = "* " + idCell
		}
		tbl.Row(
			idCell,
			string(p.Status),
			fmt.Sprintf("%d", p.GetTaskCount()),
			p.CreatedAt.Format("2006-01-02"),
			p.Goal,
		)
	}
	tbl.Render()

	fmt.Println()
	fmt.Printf("total    %d plan(s)\n", len(plans))
	if activeID != "" {
		fmt.Printf("active   %s  (marked with *)\n", utils.ShortID(activeID, utils.TaskIDLength))
	}
	return nil
}

